
// post increment
let a = 10;
let b = a++;

console.log(a); // 11
console.log(b); // 10

// pre increment
let x = 10;
let y = ++x;

console.log(x); // 11
console.log(y); // 11

// post decrement
let p = 10;
let q = p--;

console.log(p); // 9
console.log(q); // 10

// pre decrement
let c = 10;
let d = --c;

console.log(c); // 9
console.log(d); // 9

