let a = 10;
a = a+2;  // a = 12
a += 10;  // a = 22
a++;      // a =23

console.log(a);

let b = 20;
b = b-2;  // b = 18
b -= 10;  // b = 8
b--;      // b = 7

console.log(b);

let x = 10;
x += 2;
x++;    // post increment
++x;  // pre increment

console.log(x);

let y = 10;
y -= 2;
y--;   // post decrement 
--y;  // pre decrement

console.log(y);